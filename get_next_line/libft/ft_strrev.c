#include "libft.h"

char	*ft_strrev(char *s)
{
	int		i;
	int		j;
	char	aux;

	i = 0;
	j = ft_strlen(s) - 1;
	while (i < j)
	{
		aux = s[i];
		s[i] = s[j];
		s[j] = aux;
		i++;
		j--;
	}
	return (s);
}
