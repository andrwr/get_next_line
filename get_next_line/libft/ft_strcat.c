#include "libft.h"

char	*ft_strcat(char *s1, char *s2)
{
	int	x;
	int	i;

	x = ft_strlen(s1);
	i = 0;
	while (s2[i] != '\0')
	{
		s1[x] = s2[i];
		x++;
		i++;
	}
	s1[x] = '\0';
	return (s1);
}
